def count_cycle(i):
	"""Цикл"""
	while i >= 0:
		print(i, end = ' ')
		i -= 1


count_cycle(3)

print('\n ******************************************************************* ')

def count_recur(i):
	print(i, end = ' ')
	# базовый случай
	if i <= 0:
		return
	# рекрсивный случай (шаг рекурсии)
	count_recur(i - 1)

count_recur(3)


