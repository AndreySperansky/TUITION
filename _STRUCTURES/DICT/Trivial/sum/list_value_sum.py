d = {'математика': ['10', '20'], 'информатика': ['7', '12', '6'], 'литература': ['33'], 'физика': ['30', '10', '15']}
for key, value in d.items():
	print(key, '-->', sum([int(x) for x in value]), "часов")

# математика --> 30 часов
# информатика --> 25 часов
# литература --> 33 часов
# физика --> 55 часов