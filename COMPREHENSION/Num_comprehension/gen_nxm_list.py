n = int(input("Введите число строк: \n"))

# Введите число строк:
# 2



a = [list(map(int, input(f"Строка {i+1} - Введите числа через пробел: ").split())) for i in range(n)]

# Строка 1 - Введите числа через пробел: 2 3 4 5 6
# Строка 2 - Введите числа через пробел: 5 6 7 8 9

print(a, '\n')
# [[2, 3, 4, 5, 6], [5, 6, 7, 8, 9]]

for i in a:
	print(i)
# [2, 3, 4, 5, 6]
# [5, 6, 7, 8, 9]


result = []
for i in a:
	result.append(i)
print(result)
# [[2, 3, 4, 5, 6], [5, 6, 7, 8, 9]]