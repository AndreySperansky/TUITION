# 2. Сортировка с  помощью собственной функции

a = [4, 10, 43, 300, 54, 289, 34, 8, 749,]
def f(x):
	return x%10
print(sorted(a, key = f))
# [10, 300, 43, 4, 54, 34, 8, 289, 749]

def f(x):
	return -(x%10)
print(sorted(a, key = f))
# [289, 749, 8, 4, 54, 34, 43, 10, 300]

def f(x):
	return x%10, x//10%10
print(sorted(a, key = f))
# [300, 10, 43, 4, 34, 54, 8, 749, 289]