nudge = 1
wink = 2
A, B = nudge, wink 				# Присваивание кортежей	  <=> 	A, B = (nudge, wink)
print(A, B) 							# Что равносильно A = nudge; B = wink
#   (1, 2)

[C, D] = [nudge, wink] 			# Присваивание списков
print([C, D])
#   (1, 2)


# nudge - подталкивание локтем, подначивать, побуждать
# wink - моргание, подмигование, смыкание глаз, короткий сон