seq = [1, 2, 3, 4]
a, b, c, d = seq        # присвоение кортежу через имя последовательности
print(a, b, c, d)
#  1 2 3 4

a, *b = seq
print(a)
#1
print(b)
# [2, 3, 4]

a, *b, c = seq
print(a)
# 1
print(b)
# [2, 3]
print(c)
#4

print('\n===================')

seq = [1, 2, 3, 4]               # объвление и присвоение

a, *b = seq 			    # Первый, остаток
print(a, b)
#(1, [2, 3, 4])


a, b = seq[0], seq[1:] 		# Первый, остаток: традиционная реализация
print(a, b)
#(1, [2, 3, 4])